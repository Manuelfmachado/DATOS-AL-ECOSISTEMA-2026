def obtener_nombre_ciiu(codigo: str | int | None) -> str:
    """Retorna el nombre del sector para un código CIIU de 2 dígitos."""
    if codigo is None:
        return "Sector desconocido"
    codigo_str = str(int(float(codigo)))
    return CIIU_NOMBRES.get(codigo_str, f"Sector CIIU {codigo_str}")